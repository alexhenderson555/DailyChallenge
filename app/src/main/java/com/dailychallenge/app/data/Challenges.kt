    fun getChallengesForCategory(categoryId: String, languageCode: String? = null): List<String> {
        val list = if (languageCode == "en") ChallengesEn.byCategory[categoryId] else null
        return list ?: byCategory[categoryId].orEmpty()
    }

    /** Цели по выбранным категориям с фильтрацией по сложности. difficulty: "easy"/"medium"/"hard" или null (все). */
    fun getAllChallengesForCategories(categoryIds: Set<String>, languageCode: String? = null, difficulty: String? = null): List<Pair<String, String>> =
        categoryIds.flatMap { id ->
            val list = getChallengesForCategory(id, languageCode)
            filterByDifficulty(list, difficulty).map { id to it }
        }

    private fun filterByDifficulty(list: List<String>, difficulty: String?): List<String> {
        if (difficulty == null || list.isEmpty()) return list
        val third = list.size / 3
        return when (difficulty) {
            "easy" -> list.take(third.coerceAtLeast(1))
            "medium" -> list.drop(third).take(third.coerceAtLeast(1))
            "hard" -> list.drop(2 * third)
            else -> list
        }
    }

    /**
     * Перевести текст цели из одного языка в другой через параллельные индексы.
     * Если списки разного размера или текст не найден — возвращает null.
     */
    fun translateChallenge(text: String, categoryId: String, targetLanguageCode: String?): String? {
        val ruList = byCategory[categoryId] ?: return null
        val enList = ChallengesEn.byCategory[categoryId] ?: return null
        if (ruList.size != enList.size) return null

        val isTargetEn = targetLanguageCode == "en"
        val sourceList = if (isTargetEn) ruList else enList
        val targetList = if (isTargetEn) enList else ruList

        val idx = sourceList.indexOf(text)
        if (idx >= 0) return targetList[idx]

        if (targetList.contains(text)) return text
        return null
    }

